export function getPokemonName(id: number, englishName: string): string {
  if (POKEMON_KOREAN_NAMES[id]) {
    return POKEMON_KOREAN_NAMES[id];
  }
  // Capitalize English Name as fallback
  return englishName.charAt(0).toUpperCase() + englishName.slice(1);
}

export function getPokemonType(type: string): string {
  return POKEMON_KOREAN_TYPES[type] || type.toUpperCase();
}
